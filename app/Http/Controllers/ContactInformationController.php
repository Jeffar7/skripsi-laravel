    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'email' => 'required|email|unique:contactinformation',
            'message' => 'required|string|max:255'
        ]);

        $contactInformation = new ContactInformation();
        $contactInformation->name = $request->name;
        $contactInformation->email = $request->email;
        $contactInformation->message = $request->message;

        $contactInformation->save();

        return redirect('/contact')->with('status', 'Your Message Has Successfully Sent!');
    }

